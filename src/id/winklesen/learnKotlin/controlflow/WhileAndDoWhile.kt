package id.winklesen.learnKotlin.controlflow

fun main() {
    // while loop
    var counter = 1
    while (counter <= 7){
        println("Galang Riarulhaq")
        counter++
    }

    // do while loop
    var counter1 = 1
    do {
        println("Hello, World!")
        counter1++
    } while (counter1 <= 7)

}